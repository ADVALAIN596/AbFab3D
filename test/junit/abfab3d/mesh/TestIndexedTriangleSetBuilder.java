    public void testCompare1() {
        IndexedTriangleSetBuilder its1 = new IndexedTriangleSetBuilder();
        IndexedTriangleSetBuilderNew its2 = new IndexedTriangleSetBuilderNew();

        Vector3d[] points = new Vector3d[] {
            new Vector3d(0,0,0), new Vector3d(1,0,0), new Vector3d(0,1,0),
            new Vector3d(0,0,0), new Vector3d(1,0,0), new Vector3d(-1,0,0),
            new Vector3d(0,0,0), new Vector3d(-1,0,0), new Vector3d(0,-1,0),
            new Vector3d(0,0,0), new Vector3d(0,-1,0), new Vector3d(1,0,0)
        };

        for(int i=0; i < points.length / 3; i++) {
            its1.addTri(points[i*3], points[i*3+1], points[i*3+2]);
            its2.addTri(points[i*3], points[i*3+1], points[i*3+2]);
        }

        Point3d[] verts1 = its1.getVertices();
        double[] verts2 = its2.getVertices();

        for(int i=0; i < verts1.length; i++) {
            Point3d pnt = verts1[i];
            double x = verts2[i*3+0];
            double y = verts2[i*3+1];
            double z = verts2[i*3+2];

            assertEquals("x coord", x, pnt.x);
            assertEquals("y coord", y, pnt.y);
            assertEquals("z coord", z, pnt.z);
        }
        assertEquals("Vertex length", verts1.length, verts2.length / 3);

        int[][] faces1 = its1.getFaces();
        int[] faces2 = its2.getFaces();

        assertEquals("Face length", faces1.length, faces2.length / 3);
        System.out.println("FaceOld");
        for(int i=0; i < faces1.length; i++) {
            //System.out.println(faces1[i][0] + "," + faces1[i][1] + "," + faces1[i][2]);
            assertEquals("face1", faces1[i][0],faces2[i*3+0]);
            assertEquals("face2", faces1[i][1],faces2[i*3+1]);
            assertEquals("face3", faces1[i][2],faces2[i*3+2]);
        }
/*
        System.out.println("FaceNew");
        for(int i=0; i < faces2.length / 3; i++) {
            System.out.println(faces2[i*3+0] + "," + faces2[i*3+1] + "," + faces2[i*3+2]);
        }
  */
        WingedEdgeTriangleMesh mesh1 = new WingedEdgeTriangleMesh(its1.getVertices(), its1.getFaces());
        WingedEdgeTriangleMesh mesh2 = new WingedEdgeTriangleMesh(its2.getVertices(), its2.getFaces());

        assertTrue("zero count", mesh1.getVertexCount() != 0);
        assertTrue("zero count", mesh2.getVertexCount() != 0);

        assertEquals("vert count", mesh1.getVertexCount(), mesh2.getVertexCount());
        assertEquals("face count", mesh1.getFaceCount(), mesh2.getFaceCount());
        assertEquals("edge count", mesh1.getEdgeCount(), mesh2.getEdgeCount());

        Iterator<Vertex> vi1 = mesh1.vertexIterator();
        Iterator<Vertex> vi2 = mesh2.vertexIterator();

        while(vi1.hasNext()) {
            Vertex vert1 = vi1.next();
            Vertex vert2 = vi2.next();

            assertEquals("ID", vert1.getID(), vert2.getID());
            Point3d p1 = vert1.getPoint();
            Point3d p2 = vert2.getPoint();

            assertTrue("Distance", p1.distance(p2) < 1e8);
        }
    }

